public class Oab {
    
    private String nome;
    private String data_criacao;
    private String registro;
    private int tempo_servico;
    private String pais_faculdade;


    public Oab(String nome, String data_criacao, String registro, int tempo_servico, String pais_faculdade) {
        this.nome = nome;
        this.data_criacao = data_criacao;
        this.registro = registro;
        this.tempo_servico = tempo_servico;
        this.pais_faculdade = pais_faculdade;
    }





}
